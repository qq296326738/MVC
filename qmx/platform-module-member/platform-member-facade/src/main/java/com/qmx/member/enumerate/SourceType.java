package com.qmx.member.enumerate;

/**
 * 会员积分来源
 */
public enum SourceType {
    chongzhi("充值"),
    xiaofei("消费"),
    duihuan("兑换"),
    huodong("会员签到"),
    zhuce("注册"),
    fenxiang("分享"),
    xianxia("线下");

    private String title;

    SourceType(String title) {
        this.title = title;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
